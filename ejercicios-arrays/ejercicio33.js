/*
El metodo concat sirve para unificar varios arrays en uno solo.
*/

//defino dos arrays con datos
let datos1 = ["Hola", "Adios", "Avion"];
let datos2 = ["Cama", "Ventana"];

//mediante el metodo concat junto ambos arrays
let datosJuntos = datos1.concat(datos2);

//aqui se puede ver que los datos ahora estan en un mismo array llamado datosJuntos
console.log(datosJuntos);